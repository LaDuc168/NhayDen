package com.example.minh.animenotification.model;

public class MyIcon {
    private int mName;
    private int mType;

    public MyIcon(int mName, int mType) {
        this.mName = mName;
        this.mType = mType;
    }

    public int getmName() {
        return mName;
    }

    public void setmName(int mName) {
        this.mName = mName;
    }

    public int getmType() {
        return mType;
    }

    public void setmType(int mType) {
        this.mType = mType;
    }
}
